import java.util.*;

class ConvertKilometer {
    public static void main(String[] args) {

        // To show distance in kilometers, create a variable named "kilometers".
        double kilometers = 10.8;

        // To show distance in miles, create a variable named "miles".
        double miles = kilometers * 1.6;

        // Print the distance.
        System.out.println("The distance " + kilometers + " km in miles is " + miles);

    }
}